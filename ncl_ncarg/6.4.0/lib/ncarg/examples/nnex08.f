
      PROGRAM NNEX08
C
      PARAMETER(ISLIM = 11, NUMXOUT = 26, NUMYOUT = 26)
C
      REAL X(ISLIM), Y(ISLIM), Z(ISLIM)
      REAL XI(NUMXOUT), YI(NUMYOUT), ZI(NUMXOUT,NUMYOUT)
C
      DATA X/0.00,0.15,0.10,0.35,0.50,0.55,0.55,0.65,0.85,0.80,1.00/
      DATA Y/0.50,0.80,0.30,0.60,0.35,1.00,0.00,0.55,0.80,0.25,0.45/
C
      DO 130 I=1,ISLIM
          Z(I) = (X(I)-0.25)**2 + (Y(I)-0.50)**2
  130 CONTINUE
C
      XINC = 1./(NUMXOUT-1.) 
      DO 20 I=1,NUMXOUT
        XI(I) = REAL(I-1) * XINC
   20 CONTINUE
C
      YINC = 1./(NUMYOUT-1.)
      DO 30 J=1,NUMYOUT
        YI(J) = REAL(J-1) * YINC
   30 CONTINUE 
C
      CALL NNSETI('ADF',1)
      CALL NNSETC('ALG','example8.dat')
      CALL NNSETI('IGR',0)
      CALL NATGRIDS(ISLIM,X,Y,Z,NUMXOUT,NUMYOUT,XI,YI,ZI,IER)
C 
      STOP
      END
